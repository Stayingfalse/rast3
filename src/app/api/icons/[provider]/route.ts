async function fetchExternalIcon(provider: string): Promise<{ data: Buffer; contentType: string } | null> {
  try {
    const url = `https://authjs.dev/img/providers/${provider}.svg`;
    const response = await fetch(url, {
      headers: {
        'User-Agent': 'Mozilla/5.0 (compatible; IconCache/1.0)',
      },
      // Add timeout to prevent hanging
      signal: AbortSignal.timeout(10000), // 10 seconds
    });

    if (!response.ok) {
      console.warn(`Failed to fetch icon for provider ${provider}: ${response.status}`);
      return null;
    }

    const data = await response.arrayBuffer();
    const contentType = response.headers.get('content-type') || 'image/svg+xml';
    
    // Validate that we got SVG content
    const textData = new TextDecoder().decode(data);
    if (!textData.includes('<svg')) {
      console.warn(`Invalid SVG content for provider ${provider}`);
      return null;
    }
    
    return {
      data: Buffer.from(data),
      contentType,
    };
  } catch (error) {
    console.warn(`Error fetching icon for provider ${provider}:`, error);
    return null;
  }
}

function getFallbackIcon(provider: string): { data: Buffer; contentType: string } {
  const svgContent = fallbackIcons[provider] || genericFallback;
  return {
    data: Buffer.from(svgContent, 'utf-8'),
    contentType: 'image/svg+xml',
  };
}

// Helper function to convert SVG to safe data URL
function svgToDataUrl(svgContent: string): string {
  // Remove any script tags and event handlers for extra security
  const cleanSvg = svgContent
    .replace(/<script\b[^<]*(?:(?!<\/script>)<[^<]*)*<\/script>/gi, '')
    .replace(/\son\w+\s*=\s*["'][^"']*["']/gi, '')
    .replace(/javascript:/gi, '');
  
  const base64 = Buffer.from(cleanSvg).toString('base64');
  return `data:image/svg+xml;base64,${base64}`;
}

export async function GET(request: NextRequest, { params }: { params: Promise<{ provider: string }> }) {
  const { provider } = await params;

  if (!provider) {
    return new NextResponse('Provider parameter is required', { status: 400 });
  }

  // Sanitize provider name (prevent path traversal)
  const sanitizedProvider = provider.replace(/[^a-zA-Z0-9-_]/g, '');
  if (sanitizedProvider !== provider) {
    console.warn(`Invalid provider name: ${provider}`);
    return new NextResponse('Invalid provider name', { status: 400 });
  }

  // Clean up expired cache entries occasionally
  if (Math.random() < 0.1) { // 10% chance to clean up
    const now = Date.now();
    for (const [key, value] of iconCache.entries()) {
      if (now - value.timestamp > CACHE_DURATION) {
        iconCache.delete(key);
      }
    }
  }

  // Check cache first
  const cached = iconCache.get(provider);
  if (cached && (Date.now() - cached.timestamp) < CACHE_DURATION) {
    cacheStats.hits++;
    logCacheStats();
    return new NextResponse(cached.data, {
      headers: {
        'Content-Type': cached.contentType,
        'Cache-Control': 'public, max-age=86400', // 24 hours
        'X-Cache': 'HIT',
      },
    });
  }

  cacheStats.misses++;

  // Try to fetch from external source
  const external = await fetchExternalIcon(provider);
  
  if (external) {
    cacheStats.externalFetches++;
    
    // Convert SVG to safe data URL
    const svgContent = external.data.toString('utf-8');
    const dataUrl = svgToDataUrl(svgContent);
    const dataUrlBuffer = Buffer.from(dataUrl);

    // Cache the data URL
    iconCache.set(provider, {
      data: dataUrlBuffer,
      contentType: 'text/plain', // Data URL as plain text
      timestamp: Date.now(),
    });

    logCacheStats();

    return new NextResponse(dataUrl, {
      headers: {
        'Content-Type': 'text/plain',
        'Cache-Control': 'public, max-age=86400', // 24 hours
        'X-Cache': 'MISS',
        'X-Source': 'external',
      },
    });
  }

  // Use fallback icon
  cacheStats.fallbackUses++;
  const fallback = getFallbackIcon(provider);
  const svgContent = fallback.data.toString('utf-8');
  const dataUrl = svgToDataUrl(svgContent);
  const dataUrlBuffer = Buffer.from(dataUrl);
  
  // Cache fallback data URL (shorter duration)
  iconCache.set(provider, {
    data: dataUrlBuffer,
    contentType: 'text/plain',
    timestamp: Date.now(),
  });

  logCacheStats();

  return new NextResponse(dataUrl, {
    headers: {
      'Content-Type': 'text/plain',
      'Cache-Control': 'public, max-age=3600', // 1 hour for fallbacks
      'X-Cache': 'MISS',
      'X-Source': 'fallback',
    },
  });
}
